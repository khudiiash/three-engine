// Verifies that the project-type bootstrap produces a valid tsconfig.json and
// that the engine.d.ts content is correctly preserved. Simulates what the
// editor does at project open time.

import { readFileSync } from "node:fs";

// Simulate Vite's ?raw import — read the .d.ts files as strings.
const ENGINE_DTS = readFileSync("src/engine/script-types/engine.d.ts", "utf8");
const THREE_DTS = readFileSync("src/engine/script-types/three.d.ts", "utf8");

const TSCONFIG_JSON = JSON.stringify(
  {
    compilerOptions: {
      target: "ES2022",
      module: "ESNext",
      moduleResolution: "bundler",
      lib: ["ES2022", "DOM", "DOM.Iterable"],
      allowJs: true,
      checkJs: false,
      noEmit: true,
      isolatedModules: true,
      esModuleInterop: true,
      allowSyntheticDefaultImports: true,
      skipLibCheck: true,
      experimentalDecorators: true,
      useDefineForClassFields: false,
      strict: false,
      paths: {
        engine: ["./engine-types/engine.d.ts"],
      },
    },
    include: [
      "**/*.ts",
      "**/*.js",
      "**/*.tsx",
      "**/*.jsx",
    ],
    exclude: ["node_modules", "engine-types", "dist"],
  },
  null,
  2,
);

const TSCONFIG_BANNER =
  "// Generated by Three Engine on project creation / open. Re-run\n" +
  "// `scaffoldProjectTypes(projectRoot)` to refresh after engine updates.\n";

// 1. The TSCONFIG_JSON we generate is valid JSON
{
  try {
    JSON.parse(TSCONFIG_JSON);
    console.log("[1] OK: tsconfig.json content is valid JSON");
  } catch (err) {
    console.error("[1] FAIL: tsconfig.json is invalid:", err.message);
    process.exit(1);
  }
}

// 2. engine.d.ts content is non-empty and contains the `declare module "engine"` block
{
  console.assert(ENGINE_DTS.includes('declare module "engine"'), "engine.d.ts has the ambient declaration");
  console.assert(ENGINE_DTS.includes("export class Script"), "engine.d.ts declares the Script class");
  console.assert(ENGINE_DTS.includes("export function attribute"), "engine.d.ts declares the attribute function");
  console.log("[2] OK: engine.d.ts has Script + attribute declarations");
}

// 3. three.d.ts content includes the forward declarations
{
  console.assert(THREE_DTS.includes('declare module "three"'), "three.d.ts has three ambient");
  console.assert(THREE_DTS.includes('declare module "three/webgpu"'), "three.d.ts has three/webgpu ambient");
  console.log("[3] OK: three.d.ts has three + three/webgpu declarations");
}

// 4. The tsconfig.json with banner parses correctly when we strip the comment line
{
  const tsconfigPath = "/tmp/myproject/tsconfig.json";
  const finalContent = TSCONFIG_BANNER + TSCONFIG_JSON + "\n";
  // JSON.parse doesn't tolerate the leading `//` comment. JSON5/JSONC does,
  // but the IDE/TSC tools accept JSONC by default. We just verify the JSON
  // portion (after the banner) parses.
  const jsonStart = finalContent.indexOf("{");
  const jsonEnd = finalContent.lastIndexOf("}");
  const jsonPart = finalContent.slice(jsonStart, jsonEnd + 1);
  const parsed = JSON.parse(jsonPart);
  console.assert(parsed.compilerOptions.paths.engine[0] === "./engine-types/engine.d.ts", "paths.engine points correctly");
  console.assert(parsed.compilerOptions.moduleResolution === "bundler", "moduleResolution is bundler");
  console.assert(parsed.include.includes("**/*.ts"), "include covers .ts files");
  console.log("[4] OK: tsconfig.json structure is correct");
}

// 5. The actual engine.d.ts file as it would land in the user's project,
//    when opened by TypeScript, must resolve `import { Script } from "engine"`.
//    We don't run tsc here — but we verify the ambient module is well-formed.
{
  const required = [
    "Vector3", "Euler", "Quaternion",
    "Matrix4", "Color", "Object3D", "Camera",
    "Entity", "Engine", "InputManager", "ActionMap",
    "Unsub", "EngineConfig", "SceneSettings",
    "PhysicsHandle", "MathUtils", "THREE",
    "ComponentMap", "CharacterControllerComponent",
    "AttributeOptions", "attribute", "Script",
  ];
  const missing = required.filter((n) => !ENGINE_DTS.includes(n));
  if (missing.length > 0) {
    console.error("[5] FAIL: missing declarations in engine.d.ts:", missing);
    process.exit(1);
  }
  console.log("[5] OK: all required declarations present in engine.d.ts");
}

console.log("\nALL CHECKS PASSED");
console.log("\n--- tsconfig.json that would be written ---");
console.log(TSCONFIG_BANNER + TSCONFIG_JSON);